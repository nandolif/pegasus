    @Composable
    fun Component(selectedIcon: MutableState<String>): () -> Unit {
        val columns = 6
        val data = remember {
            Paginator.get(
                list = icons,
                perPage = columns * 9 + 1,
            )
        }

        val buttons = data.buttons()
        val page = data.data[data.actualPage.value] ?: emptyList()
        val toggle = Modal.Wrapper("Selecione um ícone") { t ->
            Column(
                Modifier.background(Theme.Colors.A.color)
            ) {
                LazyVerticalGrid(
                    columns = GridCells.Fixed(columns),
                    verticalArrangement = Arrangement.spacedBy(16.dp),
                    horizontalArrangement = Arrangement.spacedBy(4.dp),
                    modifier = Modifier
                        .background(
                            Theme.Colors.A.color,
                            RoundedCornerShape(bottomStart = 6.dp, bottomEnd = 6.dp)
                        )
                        .padding(horizontal = 8.dp, vertical = 16.dp)
                ) {
                    items(page) {
                        BTN(
                            text = "Icon",
                            icon = get(it) ?: Icons.Filled.Add,
                            onClick = {
                                selectedIcon.value = it
                                t()
                            },
                            height = 36,
                            containerColor = Theme.Colors.A.color,
                            textColor = Theme.Colors.D.color
                        )
                    }
                }

                Row(
                    horizontalArrangement = Arrangement.Center,
                    modifier = Modifier
                        .fillMaxWidth()
                ) {

                    Row(
                        horizontalArrangement = Arrangement.spacedBy(2.dp)
                    ) {
                        BTN("Prev", icon = Icons.Default.ArrowLeft, onClick = { data.previous() },  height = 36)
                        LazyRow(
                            horizontalArrangement = Arrangement.spacedBy(2.dp)
                        ) {
                            items(buttons) {
                                println(it)
                                BTN(
                                    it.toString(),
                                    onClick = { data.go(it) },
                                    type = if (data.actualPage.value == it) BTNType.PRIMARY else BTNType.SECONDARY,
                                    rounded = true,
                                    height = 36
                                )
                            }
                        }
                        BTN("Next", icon = Icons.Default.ArrowRight, onClick = { data.next() }, height = 32)
                    }
                }
            }
        }

        return toggle
    }
